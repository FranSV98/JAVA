public class MonstruoBasura{
  private int vida;
  private int danio;
  private int agilidad;

  public MonstruoBasura(){
    this.vida = vida;
    this.danio = danio;
    this.agilidad = agilidad;
  }

  public int getVida(){
    return this.vida;
  }

  public int getDanio(){
    return this.danio;
  }

  public int getAgilidad(){
    return this.agilidad;
  }

}
